def check_equality(num1, num2):
    if num1 == num2:
        print(num1, "is equal to", num2)
    else:
        print(num1, "is not equal to", num2)

def check_inequality(num1, num2):
    if num1 != num2:
        print(num1, "is not equal to", num2)
    else:
        print(num1, "is equal to", num2)


number1 = 3
number2 = 3
number3 = 2
number4 = 6

check_equality(number1, number2)    
check_inequality(number3, number4) 
