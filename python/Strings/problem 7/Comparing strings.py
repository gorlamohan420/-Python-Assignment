string1 = "hello"
string2 = "world"
if string1 == string2:
    print("Strings are equal")
else:
    print("Strings are not equal")
if string1 != string2:
    print("Strings are not equal")
else:
    print("Strings are equal")
if string1 < string2:
    print("String1 comes before string2")
elif string1 > string2:
    print("String1 comes after string2")
else:
    print("Strings are equal")
